def _build_all_handlers() -> dict:
    """Collect handlers from all tool modules."""
    from obsidian_bridge.mcp_tools import notes, maintenance, scout_tools, capture, temporal, memory, radar
    handlers = {}
    for mod in (notes, maintenance, scout_tools, capture, temporal, memory, radar):
        handlers.update(mod.HANDLERS)
    return handlers


def _build_all_tools() -> list[Tool]:
    """Collect Tool definitions from all modules in canonical order."""
    from obsidian_bridge.mcp_tools import notes, maintenance, scout_tools, capture, temporal, memory, radar
    result = []
    for mod in (notes, maintenance, scout_tools, capture, temporal, memory, radar):
        result.extend(mod.TOOLS)
    return result


# Lazy-initialised at first call to avoid import-time side effects
_ALL_HANDLERS: dict | None = None


def _get_all_handlers() -> dict:
    global _ALL_HANDLERS
    if _ALL_HANDLERS is None:
        _ALL_HANDLERS = _build_all_handlers()
    return _ALL_HANDLERS


@app.list_tools()
async def list_tools() -> list[Tool]:
    return _build_all_tools()


@app.call_tool()
async def call_tool(name: str, arguments: dict) -> list[TextContent]:
    handler = _get_all_handlers().get(name)
    if not handler:
        return [TextContent(type="text", text=f"Unknown tool: {name}")]
    return await handler(arguments)


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

async def run_server():
    """Run the MCP server via stdio."""
    async with stdio_server() as (read_stream, write_stream):
        await app.run(read_stream, write_stream, app.create_initialization_options())


def main():
    """CLI entry point for the MCP server."""
    import asyncio
    asyncio.run(run_server())


if __name__ == "__main__":
    main()
