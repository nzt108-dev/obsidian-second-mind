def _regenerate_index(vault: Path):
    """Regenerate index.md — catalog of all pages in the vault.

    Karpathy: 'index.md is content-oriented. It's a catalog of everything
    in the wiki — each page listed with a link, a one-line summary.'
    """
    today = date.today().isoformat()
    projects = get_projects(vault)

    lines = [
        "# 📚 Vault Index",
        f"> Auto-generated by Obsidian Second Mind. Last updated: {today}",
        "",
    ]

    # Global rules
    global_dir = vault / "_global"
    if global_dir.exists():
        global_notes = sorted(global_dir.rglob("*.md"))
        lines.append(f"## 🌐 Global Rules ({len(global_notes)})")
        lines.append("")
        for md in global_notes:
            note = parse_note(md, vault)
            if note:
                rel = md.relative_to(vault)
                lines.append(f"- [{note.title}]({rel}) — {note.note_type}")
        lines.append("")

    # Per-project sections
    total_notes = 0
    for project in projects:
        notes = get_project_notes(vault, project)
        if not notes:
            continue
        total_notes += len(notes)

        # Sort by type for consistent ordering
        type_order = {"prd": 0, "architecture": 1, "guidelines": 2, "api": 3,
                      "decision": 4, "concept": 5, "comparison": 6,
                      "synthesis": 7, "research": 8, "note": 9}
        notes.sort(key=lambda n: type_order.get(n.note_type, 99))

        types_list = ", ".join(sorted(set(n.note_type for n in notes)))
        lines.append(f"## 📁 {project} ({len(notes)} notes: {types_list})")
        lines.append("")
        for note in notes:
            rel = note.path
            # Extract first meaningful line as summary
            summary = _extract_summary(note.content)
            lines.append(f"- [{note.title}]({rel}) `{note.note_type}` — {summary}")
        lines.append("")

    # Stats
    lines.extend([
        "---",
        "## 📊 Stats",
        f"- Total projects: {len(projects)}",
        f"- Total notes: {total_notes}",
        f"- Last updated: {today}",
    ])

    index_path = vault / "index.md"
    index_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info(f"Index regenerated: {total_notes} notes across {len(projects)} projects")


def _extract_summary(content: str, max_length: int = 80) -> str:
    """Extract a one-line summary from note content."""
    for line in content.split("\n"):
        line = line.strip()
        # Skip headings, empty lines, frontmatter
        if not line or line.startswith("#") or line.startswith("---"):
            continue
        # Skip metadata-like lines
        if line.startswith(">") or line.startswith("- **") or line.startswith("| "):
            continue
        # Clean up and truncate
        summary = line[:max_length]
        if len(line) > max_length:
            summary = summary.rsplit(" ", 1)[0] + "…"
        return summary
    return "(no summary)"


def _append_to_log(
    vault: Path,
    operation: str,
    project: str = "",
    title: str = "",
    note_type: str = "",
    tags: list[str] | None = None,
    details: str = "",
):
    """Append entry to log.md — chronological operation log.

    Karpathy: 'log.md is chronological. It's an append-only record of what
    happened and when — ingests, queries, lint passes.'
    """
    today = date.today().isoformat()
    now = datetime.now().strftime("%H:%M")

    log_path = vault / "log.md"

    # Create file with header if it doesn't exist
    if not log_path.exists():
        log_path.write_text(
            "# 📋 Vault Log\n"
            "> Chronological record of vault operations. Append-only.\n\n",
            encoding="utf-8",
        )

    # Build log entry
    parts = [f"## [{today} {now}] {operation}"]
    if project:
        parts[0] += f" | {project}"
    if title:
        parts[0] += f" | {title}"
    if note_type:
        parts.append(f"Type: {note_type}")
    if tags:
        parts.append(f"Tags: {', '.join(tags)}")
    if details:
        parts.append(details)
    parts.append("")  # Empty line after entry

    # Append to log
    with open(log_path, "a", encoding="utf-8") as f:
        f.write("\n".join(parts) + "\n")

    logger.info(f"Log entry: {operation} | {project} | {title}")


